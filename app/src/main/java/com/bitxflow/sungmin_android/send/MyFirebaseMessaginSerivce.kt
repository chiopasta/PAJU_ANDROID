class MyFirebaseMessagingService : FirebaseMessagingService() {

    private val TAG = "bitx_log"

    /**
     * FirebaseInstanceIdService is deprecated.
     * this is new on firebase-messaging:17.1.0
     */
//    override fun onNewToken(token: String?) {
